    public void setImgMon(String url) {
        
        if (new DALMon().getMonByTen(labelName.getText()).isTrangThai() == false) {
            ImageIcon icon = new ImageIcon("C:\\Users\\DucThanh\\OneDrive\\Documents\\NetBeansProjects\\doAnJava\\src\\icon\\monngungban.png");
            labelImage.setIcon(icon);
        } else {
            ImageIcon icon = new ImageIcon(url);
            Image image = icon.getImage();
            image = image.getScaledInstance((int) labelImage.getPreferredSize().getWidth(), (int) labelImage.getPreferredSize().getHeight(), Image.SCALE_SMOOTH);
            icon = new ImageIcon(image);
            labelImage.setIcon(icon);
        }

    }

