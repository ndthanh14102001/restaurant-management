    private void currentMon(Mon mon)
    {
        txtTenMon.setText(mon.getTenMon());
        txtGia.setText(mon.getGia()+"");
        txtLinkAnh.setText(mon.getLinkAnh());
        cbDanhMuc.setSelectedIndex(mon.getDanhMuc());
        if(mon.isTrangThai())
        {
            cbTrangThai.setSelectedIndex(1);
        }
        else
        {
            cbTrangThai.setSelectedIndex(0);
        }
    }
    private void initComboBox()
    {
        ArrayList<DanhMuc> listDanhMuc = new BUSDanhMuc().getListDanhMuc();
        for (int i = 0; i < listDanhMuc.size(); i++) {
            cbDanhMuc.addItem(listDanhMuc.get(i).getTen());
        }
        cbTrangThai.addItem("Ngừng Bán");
        cbTrangThai.addItem("Mở Bán");
       
    }

