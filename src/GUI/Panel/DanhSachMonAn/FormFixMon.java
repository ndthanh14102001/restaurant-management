    private void btnDongYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDongYActionPerformed
        boolean trangThai;
        if(cbTrangThai.getSelectedIndex()==0)
        {
            trangThai = false;
        }
        else
        {
            trangThai = true;
        }
        Mon mon1 = new Mon(mon.getIdMon(),txtTenMon.getText(),
                Float.parseFloat(txtGia.getText()),cbDanhMuc.getSelectedIndex(), trangThai);
        mon1.setLinkAnh(txtLinkAnh.getText());
        if(new BUSMon().fixMon(mon1))
        {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công!");
            panelMon.setName(txtTenMon.getText());
            panelMon.setGia(Float.parseFloat(txtGia.getText()));
            panelMon.setImgMon(mon1.getLinkAnh());
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại!");
        }
        
    }//GEN-LAST:event_btnDongYActionPerformed

